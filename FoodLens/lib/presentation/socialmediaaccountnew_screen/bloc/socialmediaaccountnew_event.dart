/// Abstract class for all events that can be dispatched from the
///Socialmediaaccountnew widget.
///
/// Events must be immutable and implement the [Equatable] interface.
class SocialmediaaccountnewEvent extends Equatable {
  @override
  List<Object?> get props => [];
}

/// Event that is dispatched when the Socialmediaaccountnew widget is first created.
class SocialmediaaccountnewInitialEvent extends SocialmediaaccountnewEvent {
  @override
  List<Object?> get props => [];
}
